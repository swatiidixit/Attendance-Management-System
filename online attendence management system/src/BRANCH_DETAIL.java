      public void showdata(){
          try {
               con = Mysql.ConnectDB();
               String sql = "select * from branchdetail where sem = ?";
               pst = con.prepareStatement(sql);
               pst.setString(1,String.valueOf(cbsem.getSelectedItem()));
               rs = pst.executeQuery();
               table.setModel(DbUtils.resultSetToTableModel(rs));
          }
          catch(Exception ex){
              JOptionPane.showMessageDialog(null,ex);
          }
      }
