    private void updtebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updtebtnActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "update branchdetail set branch=?, branchname=?, subject=?, sem=? where subjectcode=?";
            con = Mysql.ConnectDB();
            pst = con.prepareStatement(sql);
            pst.setString(5,tfsub.getText());
            pst.setString(1,tfbranch.getText());
            pst.setString(2,tfbranchname.getText());
            pst.setString(3,tfsubcode.getText());
            pst.setString(4,String.valueOf(cbsem.getSelectedItem()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"data updated");

        }
        catch(SQLException  ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_updtebtnActionPerformed

